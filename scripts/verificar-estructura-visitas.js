// Script para verificar la estructura de la tabla visitas
const mysql = require('mysql2/promise');
require('dotenv').config();

async function verificarEstructura() {
  let connection;
  
  try {
    const config = {
      host: process.env.DB_HOST || 'localhost',
      port: process.env.DB_PORT || 3306,
      user: process.env.DB_USER || 'root',
      password: process.env.DB_PASSWORD || '',
      database: process.env.DB_NAME || 'farmadescanso',
      charset: 'utf8mb4'
    };

    console.log('üîå Conectando a la base de datos...');
    connection = await mysql.createConnection(config);
    console.log('‚úÖ Conectado a la base de datos\n');

    // Obtener estructura de la tabla
    const [columnas] = await connection.query('DESCRIBE visitas');
    
    console.log('üìã Columnas de la tabla visitas:');
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê');
    columnas.forEach(col => {
      console.log(`  ${col.Field} (${col.Type}) - Null: ${col.Null}, Default: ${col.Default || 'NULL'}`);
    });
    console.log('‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n');

    // Verificar si existe columna para enlace de reuni√≥n
    const enlaceCol = columnas.find(c => 
      c.Field.toLowerCase().includes('enlace') || 
      c.Field.toLowerCase().includes('reunion') ||
      c.Field.toLowerCase().includes('link')
    );
    
    if (enlaceCol) {
      console.log(`‚úÖ Columna para enlace de reuni√≥n encontrada: ${enlaceCol.Field}`);
    } else {
      console.log('‚ö†Ô∏è  No se encontr√≥ columna espec√≠fica para enlace de reuni√≥n');
    }

  } catch (error) {
    console.error('‚ùå Error:', error.message);
    process.exit(1);
  } finally {
    if (connection) {
      await connection.end();
      console.log('üîå Conexi√≥n cerrada');
    }
  }
}

verificarEstructura();

